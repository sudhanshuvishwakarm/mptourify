import mongoose from "mongoose";

const contactSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        lowercase: true,
        trim: true
    },
    phone: {
        type: String
    },
    subject: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    },
    status: {
        type: String,
        enum: ['new', 'resolved'],
        default: 'new'
    }
}, {
    timestamps: true
});

const Contact = mongoose.models.contacts || mongoose.model("contacts", contactSchema);
export default Contact;
